    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        PreparedStatement pps = cn.prepareStatement("INSERT INTO contac_list(Alumno, Apellido, Nombre, Email, Telefono)VALUES(?,?,?,?,?)");
        pps.setString(1, txtalumno.getText());
        pps.setString(2, txtapellido.getText());
        pps.setString(3, txtnombre.getText());
        pps.setString(4, txtemail.getText());
        pps.setString(5, txttelefono.getText());
        pps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Datos guardados");
        limpiar();
        mostrartabla();
        }
        catch(SQLException ex)
        {
            java.util.logging.Logger.getLogger(Login2.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

